package kr.co.kosmo.mvc.vo;

public class RestaurantVO {
	private int resnum;
	private String resarea,restitle,rescontent,resimg;
	private int resprice;
	public int getResnum() {
		return resnum;
	}
	public void setResnum(int resnum) {
		this.resnum = resnum;
	}
	public int getResprice() {
		return resprice;
	}
	public void setResprice(int resprice) {
		this.resprice = resprice;
	}
	public String getResarea() {
		return resarea;
	}
	public void setResarea(String resarea) {
		this.resarea = resarea;
	}
	public String getRestitle() {
		return restitle;
	}
	public void setRestitle(String restitle) {
		this.restitle = restitle;
	}
	public String getRescontent() {
		return rescontent;
	}
	public void setRescontent(String rescontent) {
		this.rescontent = rescontent;
	}
	public String getResimg() {
		return resimg;
	}
	public void setResimg(String resimg) {
		this.resimg = resimg;
	}
	
	
	

}
